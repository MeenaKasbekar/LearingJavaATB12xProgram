package ex_14_Strings;

public class Lab142_String_Substring {
    public static void main(String[] args) {

        String s1="Meena Kasbekar";
        //substring will return a string from the provided range
        System.out.println(s1.substring(2));

        String s2="RaviKasbekar";
        System.out.println(s2.substring(2,9));
    }
}
