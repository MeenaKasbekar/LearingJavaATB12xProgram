package ex_14_Strings;

public class Lab141_Subsequence_String {
    public static void main(String[] args) {


        String s1="Meena Kasbekar";
        //subsequence will return the characters of the string in the mentioned range
        System.out.println(s1.subSequence(2,7));

        String s2="RaviKasbekar";
        System.out.println(s2.subSequence(4,12));
    }
}
