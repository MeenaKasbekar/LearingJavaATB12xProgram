package ex_01_Java_Basics;

public class Lab003_print_vs_println {

    public static void main(String[] args) {

        System.out.println("Hi");
        System.out.println("Meena"); //println means next line
        System.out.println("#################");
        System.out.print("Hi,");
        System.out.print("Meena"); //only print means in the same line
    }
}
