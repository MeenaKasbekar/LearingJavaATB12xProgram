package Tasks.Basic_For_loop;

public class Task5 {
    //Print the multiplication table of 5

    //5X1=5
    //5X2=10

    public static void main(String[] args) {

        int a=5;
        for(int i=1; i<=10; i++){
            int sum=a*i;
            System.out.println(a +"X" +i +"=" +sum);
        }

    }
}
