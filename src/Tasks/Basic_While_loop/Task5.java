package Tasks.Basic_While_loop;

public class Task5 {
    //Print the multiplication table

    public static void main(String[] args) {

        int a=7;
        int i=1;
        while (i<=10){
            int sum=a*i;
            System.out.println(a +"X" +i +"=" +sum);
            i++;

        }
    }
}
